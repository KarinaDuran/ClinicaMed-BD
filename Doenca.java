public class Doenca {
    private int idDoenca;
    private String Nome;

    public void setIdDoenca(int idDoenca) {
        this.idDoenca = idDoenca;
    }

    public int getIdDoenca() {
        return idDoenca;
    }

    public void setNome(String nome) {
        Nome = nome;
    }

    public String getNome() {
        return Nome;
    }
}
